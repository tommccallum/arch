-- Create a shortcut function
local function echo_test(x)
    w.visible = true
    local p = x or ""
    if type(p) == "number" then
        p = tostring(p)
    end

    -- why does using markup not allow us to show our result?
    awful.prompt.run {
        prompt       = '<b>> </b>',
        text         = p,
        fg_cursor    = '#ff0000',        
        bg_cursor    = '#ffff00',
        highlighter  = function(text_start, text_end)
            text_start = "<span foreground='#FFFF00'>" .. text_start
            text_start = text_start .. "</span>"

            text_end = "<span foreground='#FFFF00'>" .. text_end
            text_end = text_end .. "</span>"
            return text_start, text_end
        end,
        -- To use the default `rc.lua` prompt:
        -- textbox      = mouse.screen.mypromptbox.widget,
        textbox      = atextbox,
        exe_callback = function(input)
            -- shortcut for google
            if string.sub(input,1,1) == "g" and 
                string.sub(input,2,2) == " " then
                input = string.sub(input,3,string.len(input))
                if string.sub(input,1,4) ~= "http" then
                    input = "https://" .. input
                end
                awful.util.spawn_with_shell("xdg-open " .. input )
                w.visible = false
                return
            end

            -- if we have a filename, a colon and a number
            -- then we want to open vi with that number highlighted
            for filename, linenumber in string.gmatch(input, "(.+):(%d+)") do
                if string.len(filename) > 0 and string.len(linenumber) > 0 then
                    awful.spawn(terminal .. " -x vim \"" .. filename .. "\" +" .. linenumber )
                    w.visible = false
                    return
                else
                    naughty.notify{ text="invalid filename and linenumber combination (" .. filename .. ", " .. linenumber .. ")" }
                    echo_test(p)
                    return
                end
            end
                

            -- otherwise try and use a calculator on the input
            local text = calc(input)
            if isError(text) then
                -- get the error out
                text = "There has been an error - " .. text.text
                naughty.notify{ text = text }
                echo_test(p)
                return
            end
            if not input or #input == 0 then 
                w.visible = false
                return 
            end
            
            -- naughty.notify{ text = 'The input was: '..text }
            echo_test(text)    
        end,
        hooks = {
            {{ }, 'Escape', function(_)
                w.visible = false
            end}
        }
    }   
end 
    
